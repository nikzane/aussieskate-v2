			<h1>Frequently Asked Questions</h1>
			<hr />
			<span class="faq">
				<strong>Why Aussie Skate&trade;?</strong>
				<p>Ice Skating Australia, the peak body for ice skating in Australia, established the Aussie Skate&trade; program to ensure the quality of Learn to Skate programs at rinks.  If your local ice rink is running their skate school as part of the Aussie Skate&trade; program you can have confidence that your child will receive appropriate training from accredited coaches and be fully covered with insurance.</p>
			</span>
			<span class="faq">
				<strong>Is Aussie Skate&trade; only for people who want to compete?</strong>
				<p>Absolutely not!  The purpose of an Aussie Skate&trade; skate school is to provide an affordable means for your child to acquire the foundation skills required to skate.  There are many paths for them, from just having fun with their friends, to playing Ice Hockey, joining a synchronized skating team to competing in figure skate (singles or pairs) or Ice Dance.</p>
				</span>
				<span class="faq">			
				<strong>Is there an age limit?</strong>
				<p>Children can attend skate school from the age of 3. There is no upper age limit for Aussie Skate&trade;, and in fact there is an active adult learner community - check out Coffee Skate at your local rink for adults who just love to be on the ice - maybe YOU would like to learn too.</p>
			</span>
			<span class="faq">
				<strong>Is there support for children with different needs?</strong>
				<p>Ice Skating Australia is committed to an inclusive sport.  If you are concerned about your child's ability to be part of skate school, talk to your local skate school coordinator at your preferred rink.  They will have information on how each rink is set up to support people with unique needs.</p>
			</span>
			<span class="faq">
				<strong>Who runs Aussie Skate&trade;?</strong>
				<p>While the Aussie Skate&trade; program is run by Ice Skating Australia, each skate school is run by a local rink, most of which are privately owned.  Testing of students and competitions are managed by the state ice skating sports body. It might sound a little complex but it is a well-oiled machine.</p>
				</span>
				<span class="faq">
				<strong>What about competitions?</strong>
				<p>The local ice skating clubs and the state associations run Aussie Skate&trade; competitions. Children skate based on the level they are tested at.   These competitions are organised to be a fun experience and give your child a chance to compete and show off what they have learnt.</p>
			</span>
			<span class="faq">
				<strong>I have been told that my child needs private lessons.  Is this true and does this mean they should stop skate school?</strong>
				<p>All of the coaching in Aussie Skate&trade; skate schools is delivered by accredited coaches.  ISA believes that group lessons are an excellent cost-effective way to learn the foundational skills required in all skating disciplines.  Private coaching may become a necessity as children advance to more complex elements.  ISA Preliminary level is the next level after completing Aussie Skate and the start of the progression to competitive ice skating.  This might be the time to start taking private lessons. Contacting the Skate School Director will be best to answer any questions you may have regarding private lessons.</p>
